package org.wso2.appserver.sample.chad;

import org.wso2.appserver.sample.chad.data.ChadChoice;

import java.util.Comparator;
import java.io.Serializable;

/**
 *  Comparator for comparing {@link ChadChoice}s
 *
 * @see ChadChoice
 */
public class ChadChoiceComparator implements Comparator, Serializable {

    /**
     * Compares its two arguments for order.  Returns a negative integer,
     * zero, or a positive integer as the first argument is less than, equal
     * to, or greater than the second.<p>
     * <p/>
     * The implementor must ensure that <tt>sgn(compare(x, y)) ==
     * -sgn(compare(y, x))</tt> for all <tt>x</tt> and <tt>y</tt>.  (This
     * implies that <tt>compare(x, y)</tt> must throw an exception if and only
     * if <tt>compare(y, x)</tt> throws an exception.)<p>
     * <p/>
     * The implementor must also ensure that the relation is transitive:
     * <tt>((compare(x, y)&gt;0) &amp;&amp; (compare(y, z)&gt;0))</tt> implies
     * <tt>compare(x, z)&gt;0</tt>.<p>
     * <p/>
     * Finally, the implementer must ensure that <tt>compare(x, y)==0</tt>
     * implies that <tt>sgn(compare(x, z))==sgn(compare(y, z))</tt> for all
     * <tt>z</tt>.<p>
     * <p/>
     *
     * Note: this comparator imposes orderings that are inconsistent with equals.
     *
     * @param o1 the first object to be compared.
     * @param o2 the second object to be compared.
     * @return a negative integer, zero, or a positive integer as the
     *         first argument is less than, equal to, or greater than the
     *         second.
     * @throws ClassCastException if the arguments' types prevent them from
     *                            being compared by this Comparator.
     */
    public int compare(Object o1, Object o2) {

        ChadChoice ch1 = (ChadChoice) o1;
        ChadChoice ch2 = (ChadChoice) o2;

        if (ch1.getNumberOfVotes() < ch2.getNumberOfVotes()) {
            return -1;
        }
        if (ch1.getNumberOfVotes() == ch2.getNumberOfVotes()) {
            return 0;
        }
        if (ch1.getNumberOfVotes() > ch2.getNumberOfVotes()) {
            return 1;
        }
        return 0;
    }
}
