package org.wso2.carbon.appfactory.core.dto;

import org.wso2.carbon.appfactory.core.ApplicationEventsListener;

/**
 * Holds information about a user. Refer {@link ApplicationEventsListener} for usage.
 */
public class UserInfo {
    /**
     * User id of the User
     */
    private String userName;
    private String[] roles;
    
    public UserInfo(){
        
    }
    
    public UserInfo(String userName) {
        this(userName, new String[]{});
    }

    public UserInfo(String userName, String[] roles){
        this.userName = userName;
        this.roles = roles;
    }
    
    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String[] getRoles() {
        return roles;
    }

    public void setRoles(String[] roles) {
        this.roles = roles;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((userName == null) ? 0 : userName.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        UserInfo other = (UserInfo) obj;
        if (userName == null) {
            if (other.userName != null)
                return false;
        } else if (!userName.equals(other.userName))
            return false;
        return true;
    }
    
    
}
