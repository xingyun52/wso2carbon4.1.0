package org.wso2.salesforce.webapp.salesforce.entity;

public class Account {

    private String name;
    private String accountNo;   //Account number assigned to this account
    private String type;        //Type of account, for example, Customer, Competitor, or Partner.
    private String industry;    //An industry associated with this account.
    private String website;     //The website of this account
    private String phone;       //The phone number of the account
    private String email;

    //Details for the billing address of this account
    private String street;
    private String city;
    private String postalCode;
    private String state;
    private String country;

    /**
     * Creates an Account object in which all the variable values are set to "None"
     */
    public Account() {
        this.accountNo = "None";
        this.city = "None";
        this.country = "None";
        this.industry = "None";
        this.name = "None";
        this.phone = "None";
        this.email = "None";
        this.postalCode = "None";
        this.state = "None";
        this.street = "None";
        this.type = "None";
        this.website = "None";
    }

    //Setters and Getters
    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getIndustry() {
        return industry;
    }

    public void setIndustry(String industry) {
        this.industry = industry;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

}
